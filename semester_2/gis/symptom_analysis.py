# Список запитань і відповідних симптомів
questions_to_symptoms = {
    "Чи комп'ютер працює повільно? (y/n): ": "Комп'ютер працює повільно",
    "Чи є програми, які не запускаються? (y/n): ": "Програма не запускається",
    "Чи відкриваються підозрілі вікна? (y/n): ": "Відкриваються підозрілі вікна",
    "Чи запускаються невстановлені вами програми? (y/n): ": "Запуск невстановлених програм",
    "Чи браузер перенаправляє на невідомі сайти? (y/n): ": "Браузер перенаправляє на невідомі сайти",
    "Чи файли зникають без вашої дії? (y/n): ": "Файли зникають",
    "Чи заблоковані або зашифровані ваші файли? (y/n): ": "Файли заблоковані"
}

# Функція для взаємодії з користувачем
def get_user_symptoms():
    identified_symptoms = []
    
    print("\nДайте відповідь на наступні питання щодо стану вашого пристрою:\n")
    
    for question, symptom in questions_to_symptoms.items():
        answer = input(question).strip().lower()
        if answer == 'y':
            identified_symptoms.append(symptom)
    
    return identified_symptoms
