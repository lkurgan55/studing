def get_recommendations(virus_class, fuzzy_risk_levels):
    recommendations = []

    # Загальні рекомендації, що базуються на рівні ризику
    if fuzzy_risk_levels['very_high'] > 0.7:
        recommendations.append("Рівень ризику дуже високий. Негайно від'єднайте пристрій від мережі і зверніться до фахівця.")
    elif fuzzy_risk_levels['medium_or_high'] > 0.5:
        recommendations.append("Рівень ризику середній або високий. Проведіть повне сканування пристрою антивірусом.")
    else:
        recommendations.append("Ризик помірний. Рекомендуємо провести стандартну перевірку пристрою.")

    # Специфічні рекомендації, що базуються на класі вірусу
    class_specific_recommendations = {
        'Ransomware': [
            "Не платіть викуп! Це не гарантує відновлення файлів.",
            "Скористайтеся інструментами для розшифрування файлів або відновлення з резервних копій."
        ],
        'Trojan': [
            "Проведіть глибоке сканування антивірусом та видаліть всі знайдені загрози.",
            "Перевірте автозапуск програм і видаліть підозрілі елементи."
        ],
        'Spyware': [
            "Змініть важливі паролі після очищення пристрою.",
            "Використовуйте спеціалізоване ПЗ для видалення шпигунських програм (наприклад, Malwarebytes)."
        ],
        'Adware': [
            "Очистіть кеш та куки браузера.",
            "Перевірте та видаліть підозрілі розширення браузера."
        ],
        'Шкідливе ПЗ': [
            "Запустіть повне антивірусне сканування.",
            "Оновіть бази антивірусу до актуальної версії.",
            "Оновіть операційну систему та всі важливі програми."
        ]
    }

    # Додаємо специфічні рекомендації
    specific = class_specific_recommendations.get(virus_class, [])
    recommendations.extend(specific)

    return recommendations
